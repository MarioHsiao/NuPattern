
namespace NuPattern.Runtime
{
    /// <summary>
    /// The visibility of a toolkit.
    /// </summary>
    public enum ToolkitVisibility
    {
        /// <summary>
        /// The toolkit is hidden
        /// </summary>
        Hidden = 0,

        /// <summary>
        /// The toolkit is expanded.
        /// </summary>
        Expanded = 1,

        /// <summary>
        /// The toolkit is collapsed.
        /// </summary>
        Collapsed = 2
    }
}
