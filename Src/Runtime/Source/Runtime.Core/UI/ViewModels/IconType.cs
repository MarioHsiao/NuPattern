namespace NuPattern.Runtime.UI.ViewModels
{
    /// <summary>
    /// Defines the icon type to be used in the <see cref="MenuOptionViewModel"/>.
    /// </summary>
    public enum IconType
    {
        /// <summary>
        /// The element does not have an icon.
        /// </summary>
        None,

        /// <summary>
        /// The element have an image as icon.
        /// </summary>
        Image,

        /// <summary>
        /// The element have an option button as icon.
        /// </summary>
        SingleSelect
    }
}