
namespace NuPattern.Runtime
{
    /// <summary>
    /// Definitions for URI's for LaunchPoints
    /// </summary>
    public static class LaunchPointUri
    {
        /// <summary>
        /// The scheme of the URI
        /// </summary>
        public const string UriScheme = "launchpoint";

        /// <summary>
        /// The format of the URI host 
        /// </summary>
        public const string HostFormat = UriScheme + "://";
    }
}
