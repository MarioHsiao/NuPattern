
namespace NuPattern.VisualStudio.Extensions
{
    /// <summary>
    /// The visibility of an extension.
    /// </summary>
    public enum ExtensionVisibility
    {
        /// <summary>
        /// The extension is hidden
        /// </summary>
        Hidden = 0,

        /// <summary>
        /// The extension is expanded.
        /// </summary>
        Expanded = 1,

        /// <summary>
        /// The extension is collapsed.
        /// </summary>
        Collapsed = 2
    }
}
